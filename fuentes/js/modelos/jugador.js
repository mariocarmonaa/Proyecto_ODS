class Jugador {
    #nombre
    #fecha
    #genero
    #descripcion
    #animalesSeleccionados

    constructor(nombre, fecha, genero, descripcion, animalesSeleccionados) {
        this.#nombre = nombre
        this.#fecha = fecha
        this.#genero = genero
        this.#descripcion = descripcion
        this.#animalesSeleccionados = animalesSeleccionados
    }
}